<script>
import MixinView from "../../core/mixin/View.js"


export default {
    mixins: [ MixinView ],

    name: 'RealMemoryView1',

    props: {
        wordSize: {
            type: Number,
            default: 16,
        },
        addr: {
            type: Number,
            default: 0
        },
        value: {
            type: Number,
            default: 0
        }
    },

    computed: {

        items () {
            let a = []
            for ( let i = 0; i < this.wordSize; i ++ )
                a.push( i )
            return a
        },

        displayAddr () {
            return '地址 ' + this.addr.toString()
        },

        displayValue () {
            return '数据 ' + this.value.toString()
        },

        scrollHeight () {
            return this.wordSize === 16 ? 600 : 1200
        }
    },

    data() {
        return {
            title: '内存结构图',
        }
    },

    mounted() {
    },

}
</script>

